weather_conditions = ['rainy', 'snowing', 'cloudy','sunny','foggy']
for type_of_weather in weather_conditions:
    if type_of_weather == 'rainy':
        print(type_of_weather + ' : bring an umbrella')
    elif type_of_weather == 'snowing':
        print(type_of_weather + ' : wear snowpants')
    else:
        print(type_of_weather + ' : who knows? ')
